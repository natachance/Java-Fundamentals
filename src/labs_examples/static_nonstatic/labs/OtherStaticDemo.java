package labs_examples.static_nonstatic.labs;

public class OtherStaticDemo {

    public void otherNonStaticMethod(){
        System.out.println("Calling a non-static method from the OtherStaticDemo class.");
        System.out.println();
    }

    public static void otherStaticMethod(){
        System.out.println("Calling a static method from the OtherStaticDemo class.");
        System.out.println();
    }
}
